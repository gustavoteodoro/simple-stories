export function spectralRegular(fontSize) {
    return `
        font-family: 'Spectral SC', sans-serif;
        font-size: ${fontSize}px;
        font-weight: 400;
        -webkit-font-smoothing: antialiased;
        -moz-osx-font-smoothing: grayscale;
    `;
}

